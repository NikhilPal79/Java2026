package inclass.feb22;

public class Recap {
    static void main(String[] args) {


        int x = 10;
        while (x < 100) {
            System.out.println(x++);
            x++;
        }
        int y = 10;
        while (y < 100) {
            System.out.println(++y);
            y++;
        }

    }
}
